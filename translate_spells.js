let translated = 0;
let nameTranslated = 0;

// Step 1: Translate spell names
for (const [en, pt] of Object.entries(SPELL_NAME_MAP)) {
  const escapedEn = en.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const regex = new RegExp(`"name": "${escapedEn}"`, 'g');
  const before = content;
  content = content.replace(regex, `"name": "${pt}"`);
  if (content !== before) nameTranslated++;
}

