function applyTermMap(text, termMap) {
  let result = text;
  // Apply longer phrases first to avoid partial replacements
  const sortedKeys = Object.keys(termMap).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    // Case-insensitive replacement, preserve original casing of replacement
    const regex = new RegExp(key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
    result = result.replace(regex, (match) => {
      // If original starts with uppercase, capitalize replacement
      if (match[0] === match[0].toUpperCase() && match[0] !== match[0].toLowerCase()) {
        const repl = termMap[key];
        return repl.charAt(0).toUpperCase() + repl.slice(1);
      }
      return termMap[key];
    });
  }
  return result;
}

