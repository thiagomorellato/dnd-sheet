for (const [en, pt] of Object.entries(DURATION_MAP)) {
  const escapedEn = en.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const regex = new RegExp(`"duration": "${escapedEn}"`, 'gi');
  content = content.replace(regex, `"duration": "${pt}"`);
}

// Step 4: Apply term translations to descriptions
// Find all description values and translate them
content = content.replace(/"description": "((?:[^"\\]|\\.)*)"/g, (match, desc) => {
  // Unescape
  let text = desc.replace(/\\n/g, '\n').replace(/\\"/g, '"').replace(/\\\\/g, '\\');
  // Apply term map
  text = applyTermMap(text, TERM_MAP);
  // Re-escape
  text = text.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n');
  translated++;
  return `"description": "${text}"`;
});

